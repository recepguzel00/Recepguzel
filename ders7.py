#Aritmetiksel opertörler
#sayısal aritmetiksel işlemler yapmamaızı sağlayan operatörleri

print(3+2) # (+) opertörünü toplama amacıyla kullandık

print(9 - 2 ) # (-) opertörünü çıkarma amacıyla kullandık

print (25 / 5 ) # (/) operatörünü bölme amacıyla kullandık

print (7 * 7 )  # (*) operatörünün çarpa amacıyla kullandık

print(27 // 8 ) # (//) operatörünü tam bölme amacıyla kullandık

print(5%2) #(%) operatörünü mod almak için kullandık

print (2**3) #(**) operatörünü üs almak için kullandık
